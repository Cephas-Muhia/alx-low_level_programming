#include <stdio.h>

/**
 *
 * mod - modulas of two integers
 * @a: first number
 * @b: second number
 * Return: final answer
 */
int mod(int a, int b)
{
	return(a % b);
}
/**
 *
 * add - addition of two integers
 * @a: first number
 * @b: second number
 * Return: final answer
 */
int add(int a, int b)
{
        return(a + b);
}
/**
 *
 * sub - substraction of two integers
 * @a: first number
 * @b: second number
 * Return: final answer
 */
int sub(int a, int b)
{
        return(a - b);
}
/**
 *
 * mul - multiplacation of two integers
 * @a: first number
 * @b: second number
 * Return: final answer
 */
int mul(int a, int b)
{
        return(a * b);
}
/**
 *
 * div - division of two integers
 * @a: first number
 * @b: second number
 * Return: final answer
 */
int div(int a, int b)
{
        return(a / b);
}
